# BEGIN HELPERS

Inf = 1.0/0.0

def border(len)
  (1..len).map{ "=" }.join
end

class Fibonacci < Enumerator
  def initialize(upper_bound=Inf)
    @terms = 0
    super() do |yielder|
      first, second = 1, 1
      while first < upper_bound do
        @terms += 1
        yielder.yield first
        second = first + second
        first = second - first
      end
    end
  end

  def term_number
    return @terms
  end
end

class Sieve < Enumerator
  def initialize(upper_bound=Inf)
    super() do |yielder|
      composites = {}
      (2..upper_bound).each do |x|
        if composites.include? x
          composites[x].each do |root_prime|
            (composites[x + root_prime] ||= []) << root_prime
          end
          composites.delete x
        else
          yielder.yield x
          composites[x**2] = [x]
        end
      end
    end
  end
end

